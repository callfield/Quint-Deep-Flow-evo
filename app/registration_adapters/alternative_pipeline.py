def _resolve_ambia_root(ambia_root: Path | None) -> Path | None:
    if ambia_root and str(ambia_root).strip():
        return Path(ambia_root).resolve()
    candidates = [
        os.environ.get("QDF_AMBIA_ROOT", ""),
        str(Path.home() / "AMBIA" / "AMBIA"),
        str(Path(__file__).resolve().parents[1] / "external_methods" / "AMBIA"),
    ]
    for candidate_text in candidates:
        if not candidate_text:
            continue
        candidate = Path(candidate_text).resolve()
        if (candidate / "Gui_Atlases" / "Adult_full_atlases").exists():
            return candidate
    return None


