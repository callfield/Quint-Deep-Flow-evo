    """Rewrite a DeepSlice JSON copy using AP-limited edge matching."""

    logger = logger or LOG
    json_path = Path(json_path).resolve()
    if not json_path.exists():
        raise FileNotFoundError(f"DeepSlice JSON not found: {json_path}")
    jpg_dir = Path(jpg_dir).resolve() if jpg_dir else json_path.parent
    output_stem = Path(output_stem).resolve() if output_stem else json_path.with_name(QDF1_EVO_OUTPUT_STEM)
    output_stem.parent.mkdir(parents=True, exist_ok=True)
    qc_dir = output_stem.parent / "qdf1_evo_qc"
    qc_dir.mkdir(parents=True, exist_ok=True)

    hints = load_ap_position_hints(Path(ap_hint_path).resolve()) if ap_hint_path and str(ap_hint_path).strip() else []
    hint_lookup = build_ap_hint_lookup(hints)
    template_cache = CcfCoronalTemplateCache(atlas_root=atlas_root)

    payload = json.loads(json_path.read_text(encoding="utf-8"))
    payload = sanitize_payload_for_quicknii(payload=payload, default_name=output_stem.stem)
    slices = [item for item in payload.get("slices", []) if isinstance(item, dict)]
    if not slices:
        raise ValueError(f"No slices found in DeepSlice JSON: {json_path}")

    constant_angle_slices = _constant_angle_slice_payloads(slices)
    prepared_slices: list[FitSlicePreparation] = []
    total = max(len(slices), 1)
    for index, (slice_payload, constant_slice) in enumerate(zip(slices, constant_angle_slices), start=1):
        filename = str(slice_payload.get("filename", ""))
        if progress_callback:
            progress_callback(f"Preparing {filename or index}", (index - 1) / total * 0.22)
        image_path = jpg_dir / Path(filename).name
        if not image_path.exists():
            raise FileNotFoundError(f"Slice image referenced by JSON was not found: {image_path}")
