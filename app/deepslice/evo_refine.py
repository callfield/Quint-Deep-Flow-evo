def _anchoring_center(slice_payload: dict[str, object]) -> np.ndarray:
    width = float(slice_payload.get("width", 0) or 0)
    height = float(slice_payload.get("height", 0) or 0)
    # Width/height are intentionally unused because QuickNII anchoring stores
    # full image span vectors, matching estimate_slice_ap_mm.
    _ = (width, height)
    anchoring = np.asarray(slice_payload.get("anchoring", []), dtype=np.float64)
    if anchoring.size < 9:
        return np.full(3, np.nan, dtype=np.float64)
    return anchoring[0:3] + (anchoring[3:6] * 0.5) + (anchoring[6:9] * 0.5)


def _normal_angle_delta_deg(a_slice: dict[str, object], b_slice: dict[str, object]) -> float:
    a = np.asarray(a_slice.get("anchoring", []), dtype=np.float64)
    b = np.asarray(b_slice.get("anchoring", []), dtype=np.float64)
    if a.size < 9 or b.size < 9:
        return float("nan")
    a_normal = np.cross(a[3:6], a[6:9])
    b_normal = np.cross(b[3:6], b[6:9])
    a_norm = float(np.linalg.norm(a_normal))
    b_norm = float(np.linalg.norm(b_normal))
    if a_norm <= 0.0 or b_norm <= 0.0:
        return float("nan")
    cosine = float(np.clip(np.dot(a_normal, b_normal) / (a_norm * b_norm), -1.0, 1.0))
    return float(math.degrees(math.acos(cosine)))


def _clean_template_mask(mask: np.ndarray) -> np.ndarray:
    # CCF background can contain narrow holes at ventricles/tract boundaries; for
    # AP plane selection we need the outer brain silhouette rather than labels.
    from scipy import ndimage as ndi

    mask_bool = mask > 0
    mask_bool = ndi.binary_closing(mask_bool, structure=np.ones((3, 3), dtype=bool), iterations=1)
    mask_bool = ndi.binary_fill_holes(mask_bool)
    return mask_bool.astype(np.uint8)


