def _render_atlas_mask_from_anchoring(
    *,
    template_cache: CcfCoronalTemplateCache,
    anchoring: list[float],
    output_shape: tuple[int, int],
) -> np.ndarray:
    labels = template_cache.labels
    values = np.asarray(anchoring, dtype=np.float64)
    if values.size < 9:
        return np.zeros(output_shape[:2], dtype=np.uint8)
    h, w = int(output_shape[0]), int(output_shape[1])
    yy, xx = np.mgrid[0:h, 0:w]
    x_norm = xx.astype(np.float64) / max(float(w - 1), 1.0)
    y_norm = yy.astype(np.float64) / max(float(h - 1), 1.0)
    coords = values[0:3] + (x_norm[..., None] * values[3:6]) + (y_norm[..., None] * values[6:9])
    rounded = np.rint(coords).astype(np.int32, copy=False)
    valid = (
        (rounded[..., 0] >= 0)
        & (rounded[..., 0] < labels.shape[0])
        & (rounded[..., 1] >= 0)
        & (rounded[..., 1] < labels.shape[1])
        & (rounded[..., 2] >= 0)
        & (rounded[..., 2] < labels.shape[2])
    )
    mask = np.zeros((h, w), dtype=np.uint8)
    if np.any(valid):
        sampled = labels[
            rounded[..., 0][valid],
            rounded[..., 1][valid],
            rounded[..., 2][valid],
        ]
        mask[valid] = (sampled > 0).astype(np.uint8)
    return mask


def _score_rendered_overlay(
    *,
    source_mask: np.ndarray,
    atlas_mask: np.ndarray,
    source_bbox: tuple[int, int, int, int],
    atlas_bbox: tuple[int, int, int, int],
) -> float:
    source_bool = source_mask > 0
    atlas_bool = atlas_mask > 0
    union = np.logical_or(source_bool, atlas_bool).sum()
    if union == 0:
        return -1.0
    intersection = np.logical_and(source_bool, atlas_bool).sum()
    iou = float(intersection) / float(union)
    source_edge = feature.canny(source_bool, sigma=1.0)
    atlas_edge = feature.canny(atlas_bool, sigma=1.0)
    chamfer_terms: list[float] = []
    if source_edge.any() and atlas_edge.any():
        atlas_dist = ndi.distance_transform_edt(~atlas_edge)
        source_dist = ndi.distance_transform_edt(~source_edge)
        norm = float(max(source_mask.shape[:2]))
        chamfer_terms.append(float(np.mean(atlas_dist[source_edge])) / max(norm, 1.0))
        chamfer_terms.append(float(np.mean(source_dist[atlas_edge])) / max(norm, 1.0))
    chamfer = float(np.mean(chamfer_terms)) if chamfer_terms else 1.0
    sx0, sy0, sx1, sy1 = source_bbox
    ax0, ay0, ax1, ay1 = atlas_bbox
    source_aspect = max(sx1 - sx0, 1) / max(sy1 - sy0, 1)
    atlas_aspect = max(ax1 - ax0, 1) / max(ay1 - ay0, 1)
    aspect_penalty = min(abs(math.log(max(source_aspect, 1e-6) / max(atlas_aspect, 1e-6))), 1.0)
    source_area = max(float(source_bool.sum()), 1.0)
    atlas_area = max(float(atlas_bool.sum()), 1.0)
    area_penalty = min(abs(math.log(source_area / atlas_area)), 1.0)
    return (0.62 * iou) + (0.34 * (1.0 - min(chamfer, 1.0))) - (0.05 * aspect_penalty) - (0.05 * area_penalty)


def _ap_prior_penalty(
    *,
    anchoring: list[float],
    baseline_slice: dict[str, object],
    prior_ap_mm: float,
    prior_width_mm: float,
    ap_prior_weight: float,
) -> float:
    ap_value = _estimate_ap_from_anchoring(anchoring, baseline_slice)
    if not math.isfinite(ap_value):
        return 0.0
    width = max(float(prior_width_mm), DEEPSLICE_MOUSE_VOXEL_SIZE_UM / 1000.0)
    return float(ap_prior_weight) * min(abs(ap_value - float(prior_ap_mm)) / width, 1.0)


def _estimate_ap_from_anchoring(anchoring: list[float], baseline_slice: dict[str, object]) -> float:
    payload = dict(baseline_slice)
    payload["anchoring"] = [float(value) for value in anchoring]
    return _safe_estimate_ap(payload)


def _downsample_binary_mask(mask: np.ndarray, *, max_side: int) -> tuple[np.ndarray, float]:
    h, w = mask.shape[:2]
    scale = min(1.0, float(max_side) / max(float(max(h, w)), 1.0))
    if scale >= 1.0:
        return (mask > 0).astype(np.uint8), 1.0
    new_h = max(1, int(round(h * scale)))
    new_w = max(1, int(round(w * scale)))
    resized = transform.resize(
        (mask > 0).astype(np.uint8),
        (new_h, new_w),
        order=0,
        mode="edge",
        preserve_range=True,
        anti_aliasing=False,
    )
    return (resized > 0.5).astype(np.uint8), scale


def _downsample_grayscale(image: np.ndarray, *, max_side: int) -> tuple[np.ndarray, float]:
    h, w = image.shape[:2]
    scale = min(1.0, float(max_side) / max(float(max(h, w)), 1.0))
    if scale >= 1.0:
        return image.astype(np.uint8, copy=False), 1.0
    new_h = max(1, int(round(h * scale)))
    new_w = max(1, int(round(w * scale)))
    resized = transform.resize(
        image.astype(np.float32, copy=False),
        (new_h, new_w),
        order=1,
        mode="edge",
        preserve_range=True,
        anti_aliasing=True,
    )
    return np.clip(resized, 0, 255).astype(np.uint8), scale


def quicknii_y_to_ap_mm(quicknii_y: float) -> float:
    """Convert QuickNII AP-axis voxel coordinate to bregma AP millimeters."""

    allen_x_um = (float(DEEPSLICE_MOUSE_QUICKNII_RESOLUTION[1] - 1) - float(quicknii_y)) * DEEPSLICE_MOUSE_VOXEL_SIZE_UM
    ap_um = DEEPSLICE_MOUSE_BREGMA_UM[0] - allen_x_um
    return float(ap_um / 1000.0)


def ap_mm_to_quicknii_y(ap_mm: float) -> float:
    """Convert bregma AP millimeters to QuickNII AP-axis voxel coordinate."""

    allen_x_um = DEEPSLICE_MOUSE_BREGMA_UM[0] - (float(ap_mm) * 1000.0)
    return float(DEEPSLICE_MOUSE_QUICKNII_RESOLUTION[1] - 1) - (allen_x_um / DEEPSLICE_MOUSE_VOXEL_SIZE_UM)


def _best_template_for_source(
    *,
    source_signature: np.ndarray,
    source_bbox: tuple[int, int, int, int],
    candidate_y_values: list[int],
    template_cache: CcfCoronalTemplateCache,
    prior_ap_mm: float,
    prior_width_mm: float,
    ap_prior_weight: float,
) -> tuple[CcfCoronalTemplate, float, list[tuple[int, float, float]]]:
    scored: list[tuple[CcfCoronalTemplate, float]] = []
    for y_value in candidate_y_values:
        template = template_cache.template(y_value)
        edge_score = _score_mask_signature(
            source_signature=source_signature,
            source_bbox=source_bbox,
            atlas_signature=template.signature,
            atlas_bbox=template.bbox,
        )
        prior_width = max(float(prior_width_mm), DEEPSLICE_MOUSE_VOXEL_SIZE_UM / 1000.0)
        prior_penalty = min(abs(float(template.ap_mm) - float(prior_ap_mm)) / prior_width, 1.0)
        score = float(edge_score) - (float(ap_prior_weight) * prior_penalty)
        scored.append((template, score))
    if not scored:
        raise RuntimeError("No atlas templates were available for edge refinement.")
    scored.sort(key=lambda row: row[1], reverse=True)
    top = [(item.quicknii_y, item.ap_mm, score) for item, score in scored[:8]]
    return scored[0][0], scored[0][1], top


def _candidate_y_values(
    *,
    predicted_ap_mm: float,
    hint: DeepSliceApHint | None,
    no_hint_search_mm: float,
    ap_step_vox: int,
    max_y: int,
) -> tuple[list[int], float, float]:
    if hint is not None:
        prior_ap = float(hint.ap_mm)
        prior_width_mm = max(float(hint.tolerance_mm), DEEPSLICE_MOUSE_VOXEL_SIZE_UM / 1000.0)
        center_y = ap_mm_to_quicknii_y(prior_ap)
        half_width = max(1, int(math.ceil((prior_width_mm * 1000.0) / DEEPSLICE_MOUSE_VOXEL_SIZE_UM)))
    else:
        prior_ap = predicted_ap_mm if math.isfinite(predicted_ap_mm) else 0.0
        prior_width_mm = max(float(no_hint_search_mm), DEEPSLICE_MOUSE_VOXEL_SIZE_UM / 1000.0)
        center_y = ap_mm_to_quicknii_y(prior_ap)
        half_width = max(1, int(math.ceil((prior_width_mm * 1000.0) / DEEPSLICE_MOUSE_VOXEL_SIZE_UM)))
    center = int(round(center_y))
    step = max(1, int(ap_step_vox))
    start = max(0, center - half_width)
    stop = min(int(max_y), center + half_width)
    values = list(range(start, stop + 1, step))
    center_clamped = int(np.clip(center, 0, max_y))
    if center_clamped not in values:
        values.append(center_clamped)
        values.sort()
    return values, prior_ap, prior_width_mm


