def _constant_angle_slice_payloads(slices: list[dict[str, object]]) -> list[dict[str, object]]:
    """Return slice payloads whose anchoring vectors share one common angle."""

    anchors: list[np.ndarray] = []
    for item in slices:
        anchoring = np.asarray(item.get("anchoring", []), dtype=np.float64)
        if anchoring.size >= 9 and np.all(np.isfinite(anchoring[:9])):
            anchors.append(anchoring[:9])
    if not anchors:
        return [dict(item) for item in slices]

    ref_u = anchors[0][3:6]
    ref_v = anchors[0][6:9]
    unit_us: list[np.ndarray] = []
    unit_vs: list[np.ndarray] = []
    u_norms: list[float] = []
    v_norms: list[float] = []
    for anchoring in anchors:
        u = anchoring[3:6].astype(np.float64, copy=True)
        v = anchoring[6:9].astype(np.float64, copy=True)
        if float(np.dot(u, ref_u)) < 0.0:
            u *= -1.0
        if float(np.dot(v, ref_v)) < 0.0:
            v *= -1.0
        u_norm = float(np.linalg.norm(u))
        v_norm = float(np.linalg.norm(v))
        if u_norm > 1e-6 and v_norm > 1e-6:
            unit_us.append(u / u_norm)
            unit_vs.append(v / v_norm)
            u_norms.append(u_norm)
            v_norms.append(v_norm)
    if not unit_us or not unit_vs:
        return [dict(item) for item in slices]

    common_u_dir = _normalized_vector(np.mean(np.stack(unit_us, axis=0), axis=0), fallback=ref_u)
    common_v_raw = np.mean(np.stack(unit_vs, axis=0), axis=0)
    common_v_raw = common_v_raw - (common_u_dir * float(np.dot(common_v_raw, common_u_dir)))
    common_v_dir = _normalized_vector(common_v_raw, fallback=ref_v)
    median_u_norm = float(np.median(u_norms))
    median_v_norm = float(np.median(v_norms))
    common_u = common_u_dir * median_u_norm
    common_v = common_v_dir * median_v_norm

    outputs: list[dict[str, object]] = []
    for item in slices:
        anchoring = np.asarray(item.get("anchoring", []), dtype=np.float64)
        if anchoring.size < 9 or not np.all(np.isfinite(anchoring[:9])):
            outputs.append(dict(item))
            continue
        center = anchoring[0:3] + (0.5 * anchoring[3:6]) + (0.5 * anchoring[6:9])
        origin = center - (0.5 * common_u) - (0.5 * common_v)
        updated = dict(item)
        updated["anchoring"] = [float(value) for value in np.concatenate([origin, common_u, common_v])]
        updated["qdf1_evo_common_angle_source"] = "median_deepslice_orientation"
        outputs.append(updated)
    return outputs


def _estimate_global_roll_delta(
    *,
    prepared_slices: list[FitSlicePreparation],
    template_cache: CcfCoronalTemplateCache,
    ap_prior_weight: float,
) -> float:
    """Estimate one in-plane roll delta shared by the full slice series."""

    if not prepared_slices:
        return 0.0
    candidate_rolls = [-8.0, -4.0, 0.0, 4.0, 8.0]
    best_roll = 0.0
    best_score = -np.inf
    for roll_delta in candidate_rolls:
        slice_scores: list[float] = []
        for prepared in prepared_slices:
            baseline = np.asarray(prepared.constant_angle_slice.get("anchoring", []), dtype=np.float64)
            if baseline.size < 9:
                continue
            width = int(prepared.constant_angle_slice.get("width", prepared.fit_source_mask.shape[1]) or prepared.fit_source_mask.shape[1])
            height = int(prepared.constant_angle_slice.get("height", prepared.fit_source_mask.shape[0]) or prepared.fit_source_mask.shape[0])
            source_bbox = _mask_bbox(prepared.fit_source_mask)
            best_slice_score = -np.inf
            for y_value in _sample_candidate_y_values(prepared.candidate_y_values, max_count=7):
                anchoring = _anchoring_with_ap_roll_scale(
                    baseline=baseline,
                    target_y=float(y_value),
                    roll_delta_deg=float(roll_delta),
                    scale_factor=1.0,
                    image_width=width,
                    image_height=height,
                )
                score, _aligned, _atlas_bbox = _score_after_bbox_pan(
                    anchoring=anchoring,
                    source_mask=prepared.fit_source_mask,
                    source_bbox=source_bbox,
                    template_cache=template_cache,
                    prior_ap_mm=prepared.prior_ap_mm,
                    prior_width_mm=prepared.prior_width_mm,
                    ap_prior_weight=ap_prior_weight,
                    image_width=width,
                    image_height=height,
                )
                best_slice_score = max(best_slice_score, float(score))
            if math.isfinite(best_slice_score):
                slice_scores.append(best_slice_score)
        if slice_scores:
            mean_score = float(np.mean(slice_scores))
            if mean_score > best_score:
                best_score = mean_score
                best_roll = float(roll_delta)

    # Small refinement around the winning shared roll.
    refined_rolls = [best_roll - 2.0, best_roll - 1.0, best_roll, best_roll + 1.0, best_roll + 2.0]
    for roll_delta in refined_rolls:
        slice_scores = []
        for prepared in prepared_slices:
            baseline = np.asarray(prepared.constant_angle_slice.get("anchoring", []), dtype=np.float64)
            if baseline.size < 9:
                continue
            width = int(prepared.constant_angle_slice.get("width", prepared.fit_source_mask.shape[1]) or prepared.fit_source_mask.shape[1])
            height = int(prepared.constant_angle_slice.get("height", prepared.fit_source_mask.shape[0]) or prepared.fit_source_mask.shape[0])
            source_bbox = _mask_bbox(prepared.fit_source_mask)
            y_center = _sample_candidate_y_values(prepared.candidate_y_values, max_count=3)
            best_slice_score = -np.inf
            for y_value in y_center:
                anchoring = _anchoring_with_ap_roll_scale(
                    baseline=baseline,
                    target_y=float(y_value),
                    roll_delta_deg=float(roll_delta),
                    scale_factor=1.0,
                    image_width=width,
                    image_height=height,
                )
                score, _aligned, _atlas_bbox = _score_after_bbox_pan(
                    anchoring=anchoring,
                    source_mask=prepared.fit_source_mask,
                    source_bbox=source_bbox,
                    template_cache=template_cache,
                    prior_ap_mm=prepared.prior_ap_mm,
                    prior_width_mm=prepared.prior_width_mm,
                    ap_prior_weight=ap_prior_weight,
                    image_width=width,
                    image_height=height,
                )
                best_slice_score = max(best_slice_score, float(score))
            if math.isfinite(best_slice_score):
                slice_scores.append(best_slice_score)
        if slice_scores:
            mean_score = float(np.mean(slice_scores))
            if mean_score > best_score:
                best_score = mean_score
                best_roll = float(roll_delta)
    return best_roll


def _sample_candidate_y_values(values: list[int], *, max_count: int) -> list[int]:
    if len(values) <= max_count:
        return list(values)
    indices = np.linspace(0, len(values) - 1, num=max_count)
    sampled = sorted({int(values[int(round(index))]) for index in indices})
    center_index = len(values) // 2
    sampled.append(int(values[center_index]))
    return sorted(set(sampled))


def _normalized_vector(vector: np.ndarray, *, fallback: np.ndarray) -> np.ndarray:
    norm = float(np.linalg.norm(vector))
    if norm > 1e-8:
        return vector.astype(np.float64, copy=False) / norm
    fallback_norm = float(np.linalg.norm(fallback))
    if fallback_norm > 1e-8:
        return fallback.astype(np.float64, copy=False) / fallback_norm
    return np.array([1.0, 0.0, 0.0], dtype=np.float64)


def _fit_roll_primary_silhouette_to_source(
    *,
    baseline_slice: dict[str, object],
    target_y: int,
    template_cache: CcfCoronalTemplateCache,
    source_mask_preview: np.ndarray,
) -> AnchoringFit:
    """Final conservative fit: AP/scale/pan fixed, roll only."""

    baseline = np.asarray(baseline_slice.get("anchoring", []), dtype=np.float64)
    if baseline.size < 9 or not np.all(np.isfinite(baseline[:9])):
        raise ValueError(f"Slice {baseline_slice.get('filename')!r} does not have a valid DeepSlice anchoring.")
    width = int(baseline_slice.get("width", source_mask_preview.shape[1]) or source_mask_preview.shape[1])
    height = int(baseline_slice.get("height", source_mask_preview.shape[0]) or source_mask_preview.shape[0])
    source_bbox = _mask_bbox(source_mask_preview)

    fixed_scale = 1.0

    def score_candidate(roll_delta: float, dx_px: float, dy_px: float) -> tuple[float, list[float], tuple[int, int, int, int]]:
        anchoring = _anchoring_with_ap_roll_scale(
            baseline=baseline,
            target_y=float(target_y),
            roll_delta_deg=float(roll_delta),
            scale_factor=fixed_scale,
            image_width=width,
            image_height=height,
        )
        if dx_px or dy_px:
            anchoring = _translate_anchoring_by_preview_pixels(anchoring, dx_px, dy_px, source_mask_preview.shape)
            anchoring = _set_anchoring_center_y(anchoring, float(target_y))
        atlas_mask = _render_atlas_mask_from_anchoring(
            template_cache=template_cache,
            anchoring=anchoring,
            output_shape=source_mask_preview.shape,
        )
        atlas_bbox = _mask_bbox(atlas_mask)
        score = _score_rendered_overlay(
            source_mask=source_mask_preview,
            atlas_mask=atlas_mask,
            source_bbox=source_bbox,
            atlas_bbox=atlas_bbox,
        )
        return float(score), anchoring, atlas_bbox

    candidates: list[tuple[float, list[float], float, float, float, float, tuple[int, int, int, int]]] = []
    for roll_delta in [-8.0, -6.0, -4.0, -2.0, 0.0, 2.0, 4.0, 6.0, 8.0]:
        score, anchoring, atlas_bbox = score_candidate(roll_delta, 0.0, 0.0)
        candidates.append((score, anchoring, roll_delta, fixed_scale, 0.0, 0.0, atlas_bbox))

    candidates.sort(key=lambda row: row[0], reverse=True)
    for _score, _anchoring, roll_delta, scale_factor, dx_px, dy_px, _bbox in candidates[:5]:
        for fine_roll in [roll_delta - 1.0, roll_delta, roll_delta + 1.0]:
            score, anchoring, atlas_bbox = score_candidate(fine_roll, 0.0, 0.0)
            candidates.append((score, anchoring, fine_roll, fixed_scale, 0.0, 0.0, atlas_bbox))

    candidates.sort(key=lambda row: row[0], reverse=True)
    best_score, best_anchoring, best_roll, best_scale, _best_dx, _best_dy, best_atlas_bbox = candidates[0]
    top: list[tuple[int, float, float]] = []
    seen: set[tuple[int, int, int]] = set()
    for score, _anchoring, roll_delta, scale_factor, _dx, _dy, _bbox in candidates:
        key = (int(target_y), int(round(roll_delta * 10.0)), int(round(scale_factor * 1000.0)))
        if key in seen:
            continue
        seen.add(key)
        top.append((int(target_y), quicknii_y_to_ap_mm(target_y), float(score)))
        if len(top) >= 8:
            break
    final_ap = _estimate_ap_from_anchoring(best_anchoring, baseline_slice)
    return AnchoringFit(
        anchoring=[float(value) for value in best_anchoring],
        quicknii_y=int(target_y),
        ap_mm=float(final_ap if math.isfinite(final_ap) else quicknii_y_to_ap_mm(target_y)),
        score=float(best_score),
        source_bbox=source_bbox,
        atlas_bbox=best_atlas_bbox,
        top_candidates=top,
        roll_delta_deg=float(best_roll),
        scale_factor=float(best_scale),
    )


def _fit_real_rendered_atlas_to_source(
    *,
    baseline_slice: dict[str, object],
    candidate_y_values: list[int],
    template_cache: CcfCoronalTemplateCache,
    source_mask_preview: np.ndarray,
    prior_ap_mm: float,
    prior_width_mm: float,
    ap_prior_weight: float,
    fixed_roll_delta_deg: float | None = None,
    lock_center_y: bool = False,
) -> AnchoringFit:
    """Search AP/roll/scale/pan using real 3D atlas sampling from candidate anchoring."""

    baseline = np.asarray(baseline_slice.get("anchoring", []), dtype=np.float64)
    if baseline.size < 9 or not np.all(np.isfinite(baseline[:9])):
        raise ValueError(f"Slice {baseline_slice.get('filename')!r} does not have a valid DeepSlice anchoring.")
    width = int(baseline_slice.get("width", source_mask_preview.shape[1]) or source_mask_preview.shape[1])
    height = int(baseline_slice.get("height", source_mask_preview.shape[0]) or source_mask_preview.shape[0])
    source_bbox = _mask_bbox(source_mask_preview)

    scored: list[tuple[float, list[float], int, float, float, tuple[int, int, int, int]]] = []
    roll_values = [float(fixed_roll_delta_deg)] if fixed_roll_delta_deg is not None else [-8.0, -4.0, 0.0, 4.0, 8.0]
    for y_value in candidate_y_values:
        for roll_delta in roll_values:
            for scale_factor in [0.94, 1.0, 1.06]:
                anchoring = _anchoring_with_ap_roll_scale(
                    baseline=baseline,
                    target_y=float(y_value),
                    roll_delta_deg=roll_delta,
                    scale_factor=scale_factor,
                    image_width=width,
                    image_height=height,
                )
                score, aligned, atlas_bbox = _score_after_bbox_pan(
                    anchoring=anchoring,
                    source_mask=source_mask_preview,
                    source_bbox=source_bbox,
                    template_cache=template_cache,
                    prior_ap_mm=prior_ap_mm,
                    prior_width_mm=prior_width_mm,
                    ap_prior_weight=ap_prior_weight,
                    image_width=width,
                    image_height=height,
                    target_center_y=float(y_value) if lock_center_y else None,
                )
                scored.append((score, aligned, int(y_value), float(roll_delta), float(scale_factor), atlas_bbox))

    if not scored:
        raise RuntimeError("No rendered-atlas fit candidates were generated.")
    scored.sort(key=lambda row: row[0], reverse=True)
    refined: list[tuple[float, list[float], int, float, float, tuple[int, int, int, int]]] = scored[:8]
    extra_roll_values = [0.0] if fixed_roll_delta_deg is not None else [-2.0, 0.0, 2.0]
    for _seed_score, _seed_anchoring, y_value, roll_delta, scale_factor, _seed_bbox in scored[:4]:
        for extra_roll in extra_roll_values:
            for extra_scale in [0.98, 1.0, 1.02]:
                anchoring = _anchoring_with_ap_roll_scale(
                    baseline=baseline,
                    target_y=float(y_value),
                    roll_delta_deg=roll_delta + extra_roll,
                    scale_factor=scale_factor * extra_scale,
                    image_width=width,
                    image_height=height,
                )
                score, aligned, atlas_bbox = _score_after_bbox_pan(
                    anchoring=anchoring,
                    source_mask=source_mask_preview,
                    source_bbox=source_bbox,
                    template_cache=template_cache,
                    prior_ap_mm=prior_ap_mm,
                    prior_width_mm=prior_width_mm,
                    ap_prior_weight=ap_prior_weight,
                    image_width=width,
                    image_height=height,
                    target_center_y=float(y_value) if lock_center_y else None,
                )
                refined.append((score, aligned, int(y_value), float(roll_delta + extra_roll), float(scale_factor * extra_scale), atlas_bbox))
                for dx, dy in [(-18, 0), (18, 0), (0, -18), (0, 18), (-36, 0), (36, 0), (0, -36), (0, 36)]:
                    shifted = _translate_anchoring_by_preview_pixels(aligned, dx, dy, source_mask_preview.shape)
                    if lock_center_y:
                        shifted = _set_anchoring_center_y(shifted, float(y_value))
                    atlas_mask = _render_atlas_mask_from_anchoring(
                        template_cache=template_cache,
                        anchoring=shifted,
                        output_shape=source_mask_preview.shape,
                    )
                    atlas_bbox_shifted = _mask_bbox(atlas_mask)
                    shifted_score = _score_rendered_overlay(
                        source_mask=source_mask_preview,
                        atlas_mask=atlas_mask,
                        source_bbox=source_bbox,
                        atlas_bbox=atlas_bbox_shifted,
                    ) - _ap_prior_penalty(
                        anchoring=shifted,
                        baseline_slice=baseline_slice,
                        prior_ap_mm=prior_ap_mm,
                        prior_width_mm=prior_width_mm,
                        ap_prior_weight=ap_prior_weight,
                    )
                    refined.append(
                        (
                            float(shifted_score),
                            shifted,
                            int(y_value),
                            float(roll_delta + extra_roll),
                            float(scale_factor * extra_scale),
                            atlas_bbox_shifted,
                        )
                    )

    refined.sort(key=lambda row: row[0], reverse=True)
    best_score, best_anchoring, best_y, best_roll, best_scale, best_atlas_bbox = refined[0]
    top: list[tuple[int, float, float]] = []
    seen: set[tuple[int, int, int]] = set()
    for score, _anchoring, y_value, roll_delta, scale_factor, _bbox in refined:
        key = (int(y_value), int(round(roll_delta * 10.0)), int(round(scale_factor * 1000.0)))
        if key in seen:
            continue
        seen.add(key)
        top.append((int(y_value), quicknii_y_to_ap_mm(y_value), float(score)))
        if len(top) >= 8:
            break
    final_ap = _estimate_ap_from_anchoring(best_anchoring, baseline_slice)
    return AnchoringFit(
        anchoring=[float(value) for value in best_anchoring],
        quicknii_y=int(round(ap_mm_to_quicknii_y(final_ap))) if math.isfinite(final_ap) else int(best_y),
        ap_mm=float(final_ap if math.isfinite(final_ap) else quicknii_y_to_ap_mm(best_y)),
        score=float(best_score),
        source_bbox=source_bbox,
        atlas_bbox=best_atlas_bbox,
        top_candidates=top,
        roll_delta_deg=float(best_roll),
        scale_factor=float(best_scale),
    )


def _score_after_bbox_pan(
    *,
    anchoring: list[float],
    source_mask: np.ndarray,
    source_bbox: tuple[int, int, int, int],
    template_cache: CcfCoronalTemplateCache,
    prior_ap_mm: float,
    prior_width_mm: float,
    ap_prior_weight: float,
    image_width: int,
    image_height: int,
    target_center_y: float | None = None,
) -> tuple[float, list[float], tuple[int, int, int, int]]:
    atlas_mask = _render_atlas_mask_from_anchoring(
        template_cache=template_cache,
        anchoring=anchoring,
        output_shape=source_mask.shape,
    )
    aligned = _align_rendered_bbox_to_source_bbox(
        anchoring=anchoring,
        source_bbox=source_bbox,
        atlas_bbox=_mask_bbox(atlas_mask),
        preview_shape=source_mask.shape,
    )
    if target_center_y is not None:
        aligned = _set_anchoring_center_y(aligned, float(target_center_y))
    atlas_mask_aligned = _render_atlas_mask_from_anchoring(
        template_cache=template_cache,
        anchoring=aligned,
        output_shape=source_mask.shape,
    )
    atlas_bbox_aligned = _mask_bbox(atlas_mask_aligned)
    score = _score_rendered_overlay(
        source_mask=source_mask,
        atlas_mask=atlas_mask_aligned,
        source_bbox=source_bbox,
        atlas_bbox=atlas_bbox_aligned,
    ) - _ap_prior_penalty(
        anchoring=aligned,
        baseline_slice={"anchoring": aligned, "width": image_width, "height": image_height},
        prior_ap_mm=prior_ap_mm,
        prior_width_mm=prior_width_mm,
        ap_prior_weight=ap_prior_weight,
    )
    return float(score), aligned, atlas_bbox_aligned


def _score_slice_anchoring(
    *,
    anchoring: object,
    source_mask_preview: np.ndarray,
    template_cache: CcfCoronalTemplateCache,
) -> float:
    try:
        values = [float(value) for value in list(anchoring)]  # type: ignore[arg-type]
    except Exception:
        return float("nan")
    if len(values) < 9:
        return float("nan")
    source_bbox = _mask_bbox(source_mask_preview)
    atlas_mask = _render_atlas_mask_from_anchoring(
        template_cache=template_cache,
        anchoring=values,
        output_shape=source_mask_preview.shape,
    )
    return _score_rendered_overlay(
        source_mask=source_mask_preview,
        atlas_mask=atlas_mask,
        source_bbox=source_bbox,
        atlas_bbox=_mask_bbox(atlas_mask),
    )


def _anchoring_with_ap_roll_scale(
    *,
    baseline: np.ndarray,
    target_y: float,
    roll_delta_deg: float,
    scale_factor: float,
    image_width: int,
    image_height: int,
) -> list[float]:
    origin = baseline[0:3].astype(np.float64, copy=True)
    u = baseline[3:6].astype(np.float64, copy=True)
    v = baseline[6:9].astype(np.float64, copy=True)
    center = origin + (0.5 * u) + (0.5 * v)
    center[1] = float(target_y)
    width = max(float(image_width), 1.0)
    height = max(float(image_height), 1.0)
    theta = math.radians(float(roll_delta_deg))
    cos_t = math.cos(theta)
    sin_t = math.sin(theta)
    scaled_u = u * float(scale_factor)
    scaled_v = v * float(scale_factor)
    u_rot = (cos_t * scaled_u) + (sin_t * scaled_v * (width / height))
    v_rot = (-sin_t * scaled_u * (height / width)) + (cos_t * scaled_v)
    origin_new = center - (0.5 * u_rot) - (0.5 * v_rot)
    return [float(value) for value in np.concatenate([origin_new, u_rot, v_rot])]


def _align_rendered_bbox_to_source_bbox(
    *,
    anchoring: list[float],
    source_bbox: tuple[int, int, int, int],
    atlas_bbox: tuple[int, int, int, int],
    preview_shape: tuple[int, int],
) -> list[float]:
    sx0, sy0, sx1, sy1 = source_bbox
    ax0, ay0, ax1, ay1 = atlas_bbox
    source_cx = (sx0 + sx1) * 0.5
    source_cy = (sy0 + sy1) * 0.5
    atlas_cx = (ax0 + ax1) * 0.5
    atlas_cy = (ay0 + ay1) * 0.5
    return _translate_anchoring_by_preview_pixels(
        anchoring,
        dx_px=float(source_cx - atlas_cx),
        dy_px=float(source_cy - atlas_cy),
        preview_shape=preview_shape,
    )


def _set_anchoring_center_y(anchoring: list[float], target_y: float) -> list[float]:
    values = np.asarray(anchoring, dtype=np.float64).copy()
    if values.size < 9:
        return [float(value) for value in values]
    center_y = float(values[1] + (0.5 * values[4]) + (0.5 * values[7]))
    values[1] += float(target_y) - center_y
    return [float(value) for value in values]


def _translate_anchoring_by_preview_pixels(
    anchoring: list[float],
    dx_px: float,
    dy_px: float,
    preview_shape: tuple[int, int],
) -> list[float]:
    values = np.asarray(anchoring, dtype=np.float64).copy()
    h, w = preview_shape[:2]
    u = values[3:6]
    v = values[6:9]
    values[0:3] -= u * (float(dx_px) / max(float(w - 1), 1.0))
    values[0:3] -= v * (float(dy_px) / max(float(h - 1), 1.0))
    return [float(value) for value in values]


def _clean_one_jpg_for_deepslice(
    *,
    image_path: Path,
    slice_payload: dict[str, object] | None,
    output_dir: Path,
    qc_dir: Path,
    template_cache: CcfCoronalTemplateCache,
    preview_max_side: int,
    component_keep_fraction: float,
    min_atlas_keep_fraction: float,
    atlas_margin_fraction: float,
) -> CleanedJpgResult:
    gray = _read_grayscale_uint8(image_path)
    if gray.size == 0:
        raise ValueError(f"Could not read JPEG: {image_path}")
    preview, _scale = _downsample_grayscale(gray, max_side=preview_max_side)
    source_mask = tissue_mask_from_grayscale(preview)
    component_seed = _component_seed_mask_from_preview(preview)
    component_keep_seed, component_count_before, component_count_kept = _keep_large_tissue_components(
        component_seed,
        keep_fraction=component_keep_fraction,
    )
    component_keep_seed = _dilate_binary_mask(component_keep_seed, radius_px=4)
    component_mask = np.logical_and(source_mask > 0, component_keep_seed > 0).astype(np.uint8)
    component_mask = ndi.binary_fill_holes(component_mask > 0).astype(np.uint8)
    if int(component_mask.sum()) < int(source_mask.sum()) * 0.70:
        component_mask = source_mask
        component_count_before = 1
        component_count_kept = 1
    fallback_reason = ""
    atlas_mask = np.zeros_like(component_mask, dtype=np.uint8)
    final_keep = component_mask
    atlas_keep_fraction = float("nan")
    anchoring = slice_payload.get("anchoring", []) if slice_payload else []
    if isinstance(anchoring, list) and len(anchoring) >= 9 and int(component_mask.sum()) > 0:
        atlas_mask = _render_atlas_mask_from_anchoring(
            template_cache=template_cache,
            anchoring=[float(value) for value in anchoring[:9]],
            output_shape=preview.shape[:2],
        )
        atlas_mask = _clean_rendered_atlas_mask(atlas_mask)
        margin_px = max(12, int(round(max(preview.shape[:2]) * float(atlas_margin_fraction))))
        atlas_keep = _dilate_binary_mask(atlas_mask, radius_px=margin_px)
        combined = np.logical_and(component_mask > 0, atlas_keep > 0)
        component_area = int(component_mask.sum())
        combined_area = int(combined.sum())
        atlas_keep_fraction = float(combined_area) / max(float(component_area), 1.0)
        if combined_area <= 0 or atlas_keep_fraction < float(min_atlas_keep_fraction):
            fallback_reason = "atlas_mask_too_small_or_misaligned"
            final_keep = component_mask
        else:
            final_keep = combined.astype(np.uint8)
    else:
        fallback_reason = "no_valid_anchoring"

    full_keep = _resize_nearest(final_keep.astype(np.uint8), gray.shape[0], gray.shape[1])
    cleaned = gray.copy()
    cleaned[full_keep <= 0] = 0
    cleaned_path = output_dir / image_path.name
    Image.fromarray(cleaned).save(cleaned_path, quality=DEFAULT_CLEANUP_JPEG_QUALITY, optimize=True)

    qc_path = qc_dir / f"{image_path.stem}_cleanup_qc.jpg"
    _write_cleanup_qc_overlay(
        preview=preview,
        source_mask=source_mask,
        atlas_mask=atlas_mask,
        final_keep=final_keep,
        output_path=qc_path,
    )
    return CleanedJpgResult(
        filename=image_path.name,
        original_jpg=image_path,
        cleaned_jpg=cleaned_path,
        qc_overlay_path=qc_path,
        source_area_px=int(source_mask.sum()),
        component_area_px=int(component_mask.sum()),
        atlas_mask_area_px=int(atlas_mask.sum()),
        final_keep_area_px=int(final_keep.sum()),
        component_count_before=component_count_before,
        component_count_kept=component_count_kept,
        atlas_keep_fraction=atlas_keep_fraction,
        fallback_reason=fallback_reason,
    )


def _keep_large_tissue_components(mask: np.ndarray, *, keep_fraction: float) -> tuple[np.ndarray, int, int]:
    mask_bool = mask > 0
    labels, count = ndi.label(mask_bool)
    if count <= 1:
        return mask_bool.astype(np.uint8), int(count), int(count)
    areas = np.bincount(labels.ravel())
    component_areas = areas[1:]
    if component_areas.size == 0:
        return np.zeros_like(mask, dtype=np.uint8), int(count), 0
    largest_label = int(np.argmax(component_areas) + 1)
    largest_area = float(areas[largest_label])
    min_keep = max(largest_area * float(keep_fraction), float(mask.size) * 0.002)
    keep_labels = {largest_label}
    for label_index, area in enumerate(areas[1:], start=1):
        if float(area) >= min_keep:
            keep_labels.add(label_index)
    keep_mask = np.isin(labels, list(keep_labels))
    return keep_mask.astype(np.uint8), int(count), int(len(keep_labels))


def _component_seed_mask_from_preview(preview: np.ndarray) -> np.ndarray:
    """A less-filled mask used only to identify disconnected contaminating slices."""

    gray = preview.astype(np.float32, copy=False)
    if gray.size == 0 or float(np.max(gray)) <= 0.0:
        return np.zeros(gray.shape, dtype=np.uint8)
    sigma = max(1.5, float(min(gray.shape[:2])) / 140.0)
    blurred = ndi.gaussian_filter(gray, sigma=sigma)
    positive = blurred[blurred > 0]
    if positive.size == 0:
        return np.zeros(gray.shape, dtype=np.uint8)
    try:
        otsu_threshold = float(filters.threshold_otsu(blurred))
    except ValueError:
        otsu_threshold = float(np.percentile(positive, 50))
    floor_threshold = max(3.0, float(np.percentile(positive, 12)))
    threshold = max(floor_threshold, otsu_threshold * 0.65)
    mask = blurred >= threshold
    mask = ndi.binary_opening(mask, structure=np.ones((3, 3), dtype=bool), iterations=1)
    return mask.astype(np.uint8)


def _clean_rendered_atlas_mask(mask: np.ndarray) -> np.ndarray:
    mask_bool = mask > 0
    if not mask_bool.any():
        return np.zeros_like(mask, dtype=np.uint8)
    mask_bool = ndi.binary_closing(mask_bool, structure=np.ones((3, 3), dtype=bool), iterations=1)
    mask_bool = ndi.binary_fill_holes(mask_bool)
    return mask_bool.astype(np.uint8)


def _dilate_binary_mask(mask: np.ndarray, *, radius_px: int) -> np.ndarray:
    radius = max(1, int(radius_px))
    structure = morphology.disk(radius).astype(bool)
    return ndi.binary_dilation(mask > 0, structure=structure, iterations=1).astype(np.uint8)


