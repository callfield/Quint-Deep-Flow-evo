def _scaled_progress(
    callback: Callable[[str, float], None] | None,
    start: float,
    end: float,
) -> Callable[[str, float], None] | None:
    if callback is None:
        return None

    def scaled(message: str, fraction: float) -> None:
        value = start + ((end - start) * max(0.0, min(float(fraction), 1.0)))
        callback(message, value)

    return scaled


